const CACHE_NAME = 'eduflow-cache-v2';
const ASSETS = [
  '/',
  '/index.html',
  '/dashboard.html',
  '/app.js',
  '/dashboard.js',
  '/styles.css',
  '/dashboard.css',
  '/logo.svg',
  '/manifest.json'
];

self.addEventListener('install', event => {
  event.waitUntil(
    caches.open(CACHE_NAME).then(cache => {
      return cache.addAll(ASSETS);
    })
  );
  self.skipWaiting();
});

self.addEventListener('activate', event => {
  event.waitUntil(
    caches.keys().then(keys => {
      return Promise.all(
        keys.map(key => {
          if (key !== CACHE_NAME) {
            return caches.delete(key);
          }
        })
      );
    })
  );
  self.clients.claim();
});

self.addEventListener('fetch', event => {
  // Pass dynamic API routes straight to server
  if (event.request.url.includes('/api/')) {
    return;
  }
  
  // Network-First caching strategy (always fetch fresh code when online)
  event.respondWith(
    fetch(event.request)
      .then(response => {
        if (response && response.ok) {
          const clone = response.clone();
          caches.open(CACHE_NAME).then(cache => {
            cache.put(event.request, clone);
          });
        }
        return response;
      })
      .catch(() => {
        return caches.match(event.request);
      })
  );
});
